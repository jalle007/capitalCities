public class Location {
    String City;
    float Lat;
    float Lng;

    public String getCity() {
        return City;
    }
    public void setCity(String City) {
        this.City = City;
    }

    public float getLat() {
        return Lat;
    }
    public void setLat(float Lat) {
        this.Lat = Lat;
    }

    public float getLong() {
        return Lng;
    }
    public void setLong(float Lng) {
        this.Lng = Lng;
    }

}
